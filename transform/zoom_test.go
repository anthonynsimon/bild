package transform

import (
	"image"
	"testing"

	"github.com/anthonynsimon/bild/util"
)

func TestZoom(t *testing.T) {
	cases := []struct {
		description string
		factor      float64
		options     *ZoomOptions
		value       image.Image
		expected    *image.RGBA
	}{
		{
			description: "factor 1.0 (no-op)",
			factor:      1.0,
			options:     nil,
			value: &image.RGBA{
				Rect:   image.Rect(0, 0, 2, 2),
				Stride: 8,
				Pix: []uint8{
					0x80, 0x80, 0x80, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF,
					0xFF, 0xFF, 0xFF, 0xFF, 0x80, 0x80, 0x80, 0x80,
				},
			},
			expected: &image.RGBA{
				Rect:   image.Rect(0, 0, 2, 2),
				Stride: 8,
				Pix: []uint8{
					0x80, 0x80, 0x80, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF,
					0xFF, 0xFF, 0xFF, 0xFF, 0x80, 0x80, 0x80, 0x80,
				},
			},
		},
		{
			description: "factor 2.0 at center, 2x2",
			factor:      2.0,
			options:     nil,
			value: &image.RGBA{
				Rect:   image.Rect(0, 0, 2, 2),
				Stride: 8,
				Pix: []uint8{
					0x80, 0x80, 0x80, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF,
					0x40, 0x40, 0x40, 0xFF, 0xC0, 0xC0, 0xC0, 0xFF,
				},
			},
			expected: &image.RGBA{
				Rect:   image.Rect(0, 0, 2, 2),
				Stride: 8,
				Pix: []uint8{
					// 2x2 is too small for zoom to crop: integer truncation maps each pixel to itself
					0x80, 0x80, 0x80, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF,
					0x40, 0x40, 0x40, 0xFF, 0xC0, 0xC0, 0xC0, 0xFF,
				},
			},
		},
		{
			description: "factor 2.0 at center, 4x4",
			factor:      2.0,
			options:     nil,
			value: &image.RGBA{
				Rect:   image.Rect(0, 0, 4, 4),
				Stride: 16,
				Pix: []uint8{
					0x10, 0x10, 0x10, 0xFF, 0x20, 0x20, 0x20, 0xFF, 0x30, 0x30, 0x30, 0xFF, 0x40, 0x40, 0x40, 0xFF,
					0x50, 0x50, 0x50, 0xFF, 0x60, 0x60, 0x60, 0xFF, 0x70, 0x70, 0x70, 0xFF, 0x80, 0x80, 0x80, 0xFF,
					0x90, 0x90, 0x90, 0xFF, 0xA0, 0xA0, 0xA0, 0xFF, 0xB0, 0xB0, 0xB0, 0xFF, 0xC0, 0xC0, 0xC0, 0xFF,
					0xD0, 0xD0, 0xD0, 0xFF, 0xE0, 0xE0, 0xE0, 0xFF, 0xF0, 0xF0, 0xF0, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF,
				},
			},
			expected: &image.RGBA{
				Rect:   image.Rect(0, 0, 4, 4),
				Stride: 16,
				Pix: []uint8{
					// 2x zoom from center(2,2): dst(x,y) -> src((x-2)*0.5+2, (y-2)*0.5+2)
					// dst(0,0) -> src(1.25,1.25) -> src(1,1) = 0x60
					// dst(1,0) -> src(1.75,1.25) -> src(1,1) = 0x60
					// dst(2,0) -> src(2.25,1.25) -> src(2,1) = 0x70
					// dst(3,0) -> src(2.75,1.25) -> src(2,1) = 0x70
					0x60, 0x60, 0x60, 0xFF, 0x60, 0x60, 0x60, 0xFF, 0x70, 0x70, 0x70, 0xFF, 0x70, 0x70, 0x70, 0xFF,
					0x60, 0x60, 0x60, 0xFF, 0x60, 0x60, 0x60, 0xFF, 0x70, 0x70, 0x70, 0xFF, 0x70, 0x70, 0x70, 0xFF,
					0xA0, 0xA0, 0xA0, 0xFF, 0xA0, 0xA0, 0xA0, 0xFF, 0xB0, 0xB0, 0xB0, 0xFF, 0xB0, 0xB0, 0xB0, 0xFF,
					0xA0, 0xA0, 0xA0, 0xFF, 0xA0, 0xA0, 0xA0, 0xFF, 0xB0, 0xB0, 0xB0, 0xFF, 0xB0, 0xB0, 0xB0, 0xFF,
				},
			},
		},
		{
			description: "factor 0.5 at center, 4x4",
			factor:      0.5,
			options:     nil,
			value: &image.RGBA{
				Rect:   image.Rect(0, 0, 4, 4),
				Stride: 16,
				Pix: []uint8{
					0x10, 0x10, 0x10, 0xFF, 0x20, 0x20, 0x20, 0xFF, 0x30, 0x30, 0x30, 0xFF, 0x40, 0x40, 0x40, 0xFF,
					0x50, 0x50, 0x50, 0xFF, 0x60, 0x60, 0x60, 0xFF, 0x70, 0x70, 0x70, 0xFF, 0x80, 0x80, 0x80, 0xFF,
					0x90, 0x90, 0x90, 0xFF, 0xA0, 0xA0, 0xA0, 0xFF, 0xB0, 0xB0, 0xB0, 0xFF, 0xC0, 0xC0, 0xC0, 0xFF,
					0xD0, 0xD0, 0xD0, 0xFF, 0xE0, 0xE0, 0xE0, 0xFF, 0xF0, 0xF0, 0xF0, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF,
				},
			},
			expected: &image.RGBA{
				Rect:   image.Rect(0, 0, 4, 4),
				Stride: 16,
				Pix: []uint8{
					// 0.5x zoom from center(2,2): dst(x,y) -> src((x-2)*2+2, (y-2)*2+2)
					// dst(0,0) -> src(-1.0,-1.0) -> out of bounds
					// dst(1,0) -> src(1.0,-1.0) -> out of bounds
					// dst(2,0) -> src(3.0,-1.0) -> out of bounds
					// dst(3,0) -> src(5.0,-1.0) -> out of bounds
					0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
					// dst(0,1) -> src(-1.0,1.0) -> out of bounds
					// dst(1,1) -> src(1.0,1.0) -> src(1,1) = 0x60
					// dst(2,1) -> src(3.0,1.0) -> src(3,1) = 0x80
					// dst(3,1) -> src(5.0,1.0) -> out of bounds
					0x00, 0x00, 0x00, 0x00, 0x60, 0x60, 0x60, 0xFF, 0x80, 0x80, 0x80, 0xFF, 0x00, 0x00, 0x00, 0x00,
					// dst(0,2) -> src(-1.0,3.0) -> out of bounds
					// dst(1,2) -> src(1.0,3.0) -> src(1,3) = 0xE0
					// dst(2,2) -> src(3.0,3.0) -> src(3,3) = 0xFF
					// dst(3,2) -> src(5.0,3.0) -> out of bounds
					0x00, 0x00, 0x00, 0x00, 0xE0, 0xE0, 0xE0, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0x00, 0x00, 0x00, 0x00,
					// dst(0,3) -> src(-1.0,5.0) -> out of bounds (all)
					0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
				},
			},
		},
		{
			description: "factor 2.0 with custom pivot at (0,0)",
			factor:      2.0,
			options:     &ZoomOptions{Pivot: &image.Point{0, 0}},
			value: &image.RGBA{
				Rect:   image.Rect(0, 0, 4, 4),
				Stride: 16,
				Pix: []uint8{
					0x10, 0x10, 0x10, 0xFF, 0x20, 0x20, 0x20, 0xFF, 0x30, 0x30, 0x30, 0xFF, 0x40, 0x40, 0x40, 0xFF,
					0x50, 0x50, 0x50, 0xFF, 0x60, 0x60, 0x60, 0xFF, 0x70, 0x70, 0x70, 0xFF, 0x80, 0x80, 0x80, 0xFF,
					0x90, 0x90, 0x90, 0xFF, 0xA0, 0xA0, 0xA0, 0xFF, 0xB0, 0xB0, 0xB0, 0xFF, 0xC0, 0xC0, 0xC0, 0xFF,
					0xD0, 0xD0, 0xD0, 0xFF, 0xE0, 0xE0, 0xE0, 0xFF, 0xF0, 0xF0, 0xF0, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF,
				},
			},
			expected: &image.RGBA{
				Rect:   image.Rect(0, 0, 4, 4),
				Stride: 16,
				Pix: []uint8{
					// 2x zoom from pivot(0,0): dst(x,y) -> src(x*0.5, y*0.5)
					// dst(0,0) -> src(0.25,0.25) -> src(0,0) = 0x10
					// dst(1,0) -> src(0.75,0.25) -> src(0,0) = 0x10
					// dst(2,0) -> src(1.25,0.25) -> src(1,0) = 0x20
					// dst(3,0) -> src(1.75,0.25) -> src(1,0) = 0x20
					0x10, 0x10, 0x10, 0xFF, 0x10, 0x10, 0x10, 0xFF, 0x20, 0x20, 0x20, 0xFF, 0x20, 0x20, 0x20, 0xFF,
					0x10, 0x10, 0x10, 0xFF, 0x10, 0x10, 0x10, 0xFF, 0x20, 0x20, 0x20, 0xFF, 0x20, 0x20, 0x20, 0xFF,
					0x50, 0x50, 0x50, 0xFF, 0x50, 0x50, 0x50, 0xFF, 0x60, 0x60, 0x60, 0xFF, 0x60, 0x60, 0x60, 0xFF,
					0x50, 0x50, 0x50, 0xFF, 0x50, 0x50, 0x50, 0xFF, 0x60, 0x60, 0x60, 0xFF, 0x60, 0x60, 0x60, 0xFF,
				},
			},
		},
		{
			description: "factor 2.0 with ResizeBounds",
			factor:      2.0,
			options:     &ZoomOptions{ResizeBounds: true},
			value: &image.RGBA{
				Rect:   image.Rect(0, 0, 2, 2),
				Stride: 8,
				Pix: []uint8{
					0x80, 0x80, 0x80, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF,
					0x40, 0x40, 0x40, 0xFF, 0xC0, 0xC0, 0xC0, 0xFF,
				},
			},
			expected: &image.RGBA{
				Rect:   image.Rect(0, 0, 4, 4),
				Stride: 16,
				Pix: []uint8{
					// 2x zoom with ResizeBounds: canvas is 4x4, pivot at center(1,1)
					// dst maps through offset(1,1) then inverse scale
					// dst(0,0) -> src(-0.25,-0.25) -> src(0,0) via adj -> 0x80
					0x80, 0x80, 0x80, 0xFF, 0x80, 0x80, 0x80, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF,
					0x80, 0x80, 0x80, 0xFF, 0x80, 0x80, 0x80, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF,
					0x40, 0x40, 0x40, 0xFF, 0x40, 0x40, 0x40, 0xFF, 0xC0, 0xC0, 0xC0, 0xFF, 0xC0, 0xC0, 0xC0, 0xFF,
					0x40, 0x40, 0x40, 0xFF, 0x40, 0x40, 0x40, 0xFF, 0xC0, 0xC0, 0xC0, 0xFF, 0xC0, 0xC0, 0xC0, 0xFF,
				},
			},
		},
	}

	for _, c := range cases {
		actual := Zoom(c.value, c.factor, c.options)
		if !util.RGBAImageEqual(actual, c.expected) {
			t.Errorf("%s:\nexpected:%v\nactual:%v", "Zoom "+c.description, util.RGBAToString(c.expected), util.RGBAToString(actual))
		}
	}
}

func BenchmarkZoom256(b *testing.B) {
	benchZoom(256, 256, 2.0, b)
}

func BenchmarkZoom512(b *testing.B) {
	benchZoom(512, 512, 2.0, b)
}

